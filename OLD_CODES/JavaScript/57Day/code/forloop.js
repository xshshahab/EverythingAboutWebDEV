// Example of a basic for loop (counting from 0 to 4)
for (let i = 0; i < 5; i++) {
  console.log("Iteration:", i);
}

// Example of a reverse for loop (counting from 10 to 1)
for (let i = 10; i >= 1; i--) {
  console.log("Reverse Iteration:", i);
}

// Example of a for loop with an array (printing array values)
let fruits = ["Apple", "Banana", "Orange", "Mango"];
for (let i = 0; i < fruits.length; i++) {
  console.log("Fruit:", fruits[i]);
}
