// Example of a basic while loop (counting from 0 to 4)
let i = 0;
while (i < 5) {
  console.log("Iteration:", i);
  i++;
}

// Example of a reverse while loop (counting from 10 to 1)
let j = 10;
while (j >= 1) {
  console.log("Reverse Iteration:", j);
  j--;
}

// Example of a while loop with an array (printing array values)
let fruits = ["Apple", "Banana", "Orange", "Mango"];
let index = 0;
while (index < fruits.length) {
  console.log("Fruit:", fruits[index]);
  index++;
}
