// Example usage of comparison operators

let a = 5;
let b = 3;
let c = "5";

// Equal to (==)
console.log("a == b: ", a == b); // false

// Strict Equal to (===)
console.log("a === c: ", a === c); // false

// Not Equal to (!=)
console.log("a != b: ", a != b); // true

// Strict Not Equal to (!==)
console.log("a !== c: ", a !== c); // true

// Greater Than (>)
console.log("a > b: ", a > b); // true

// Less Than (<)
console.log("a < b: ", a < b); // false

// Greater Than or Equal To (>=)
console.log("a >= b: ", a >= b); // true

// Less Than or Equal To (<=)
console.log("a <= b: ", a <= b); // false
